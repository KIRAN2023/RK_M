function filter() {
    let filter = document.getElementById('findData').value.toUpperCase();
    let products = document.querySelectorAll(".productBox");

    if (filter.length != 0) {
        let filterData = [];
        if (filterData.length != 0 && list.length != 0) {
            filterData.push(list);
        }
        for (let productsDiv of products) {
            let datas = productsDiv.querySelector(".findProduct");
            if (datas.textContent.toUpperCase().indexOf(filter) > -1) {
                if (!filterData.includes(productsDiv.getAttribute("filtervalue"))) {
                    filterData.push(productsDiv.getAttribute("filtervalue"));
                }
                productsDiv.classList.remove("product-not-active");
            } else {
                document.querySelector(`.data-btn-${productsDiv.getAttribute("filtervalue")}`).checked = false;
                filterData.splice(productsDiv.getAttribute("filtervalue"));
                list.splice(document.querySelector(`.data-btn-${productsDiv.getAttribute("filtervalue")}`).value);
                productsDiv.classList.add("product-not-active");
            }
        }
        list.splice(0, list.length);
        for (let filterValue of filterData) {
            document.querySelector(`.data-btn-${filterValue}`).checked = true;
            list.push(document.querySelector(`.data-btn-${filterValue}`).value);
        }
    } else {
        products.forEach((p) => { document.querySelector(`.data-btn-${p.getAttribute("filtervalue")}`).checked = false; p.classList.remove("product-not-active") });
    }
}

